import { component$ } from "@builder.io/qwik";
import {
  QwikCityProvider,
  RouterOutlet,
  ServiceWorkerRegister,
} from "@builder.io/qwik-city";
import { RouterHead } from "./components/router-head/router-head";

import type { GoogleMapsLoaderProps} from "qwik-google-maps-loader";
import { useGoogleMapsProvider } from "qwik-google-maps-loader";

import "./global.css";

export const googleMapsOptions: GoogleMapsLoaderProps = {
  apiKey: import.meta.env.PUBLIC_GOOGLE_MAPS_API_KEY,
}

export default component$(() => {
  /**
   * The root of a QwikCity site always start with the <QwikCityProvider> component,
   * immediately followed by the document's <head> and <body>.
   *
   * Don't remove the `<head>` and `<body>` elements.
   */

  useGoogleMapsProvider(googleMapsOptions);

  return (
    <QwikCityProvider>
      <head>
        <meta charSet="utf-8" />
        <link rel="manifest" href="/manifest.json" />
        <RouterHead />
      </head>
      <body lang="en">
        <RouterOutlet />
        <ServiceWorkerRegister />
      </body>
    </QwikCityProvider>
  );
});
